open_chars=['(', '<', '[', '{']
pair_dict = {
    '(': ')',
    '[': ']',
    '<': '>',
    '{': '}'
    }
    
points_dict = {
    ')': 3,
    ']': 57,
    '}': 1197,
    '>': 25137
    }
complete_points_dict = {
    ')': 1,
    ']': 2,
    '}': 3,
    '>': 4
}

def day10():
    day10_corrupt_score = 0
    day10_incomplete_scores = []

    for idx, inp in enumerate(day10_input):
        #print(f"Testing string #{idx}")
        open_array = ''
        corrupted = False
        for c_idx, c in enumerate(inp):
            if c in open_chars:
                open_array += c
            else:
                if c==pair_dict[open_array[-1]]:
                    # paired, popping open_array
                    open_array = open_array[:-1]
                else:
                    # got corruption, catch it, add to score
                    # print(f"got corruption at char #{c_idx}: expect {pair_dict[open_array[-1]]}, got {c} instead")
                    corrupted = True
                    day10_corrupt_score += points_dict[c]
                    # finish up string, move to next
                    break
        if not corrupted:
            if open_array == '':
                print(f"string #{idx} is complete")
            else:
                #print(f"string #{idx} is incomplete, open_array = '{open_array}' building closing string...")
                close_array = ''
                close_score = 0
                for i in range(len(open_array)-1,-1,-1):
                    close_array += pair_dict[open_array[i]]
                    close_score = close_score*5 + complete_points_dict[pair_dict[open_array[i]]]
                    #print(f"Adding close char='{pair_dict[open_array[i]]}', updated close_score = {close_score}")
                #print(f"close_array = '{close_array}', close_score = {close_score}")
                day10_incomplete_scores.append(close_score)

    print(f"day10_1 score = {day10_corrupt_score}")
    print(f"day10_2 incomplete count = {len(day10_incomplete_scores)}, mid score = {sorted(day10_incomplete_scores)[math.floor(len(day10_incomplete_scores)/2)]}")
